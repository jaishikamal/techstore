// const [theme, setTheme] = useLocalStorage('theme', 'light');
// const [user, setUser] = useLocalStorage('user', null);
// 
// // Update theme
// setTheme('dark');
// 
// // Update user
// setUser({ id: 1, name: 'John Doe' });
// 
// // Remove item
// setUser(null);

export default useLocalStorage; 